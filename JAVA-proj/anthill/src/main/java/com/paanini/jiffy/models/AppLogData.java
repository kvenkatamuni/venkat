package com.paanini.jiffy.models;

public class AppLogData {
    String oldName;
    String newName;
    String oldDescription;
    String newDescription;
    String oldThump;
    String newThump;
    String appGroupName;

    public String getOldName() {
        return oldName;
    }

    public void setOldName(String oldName) {
        this.oldName = oldName;
    }

    public String getNewName() {
        return newName;
    }

    public void setNewName(String newName) {
        this.newName = newName;
    }

    public String getOldDescription() {
        return oldDescription;
    }

    public void setOldDescription(String oldDescription) {
        this.oldDescription = oldDescription;
    }

    public String getNewDescription() {
        return newDescription;
    }

    public void setNewDescription(String newDescription) {
        this.newDescription = newDescription;
    }

    public String getOldThump() {
        return oldThump;
    }

    public void setOldThump(String oldThump) {
        this.oldThump = oldThump;
    }

    public String getNewThump() {
        return newThump;
    }

    public void setNewThump(String newThump) {
        this.newThump = newThump;
    }

    public String getAppGroupName() {
        return appGroupName;
    }

    public void setAppGroupName(String appGroupName) {
        this.appGroupName = appGroupName;
    }
}
